    private void btnSupprimerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSupprimerActionPerformed
        // TODO add your handling code here:
        if(Table_pres.getSelectedRow()<0)
        {
             JOptionPane.showMessageDialog(null,"Selectionnez un prestataire pour le supprimer");
        }
        else
        {
        
        int ligne_selectionee=Table_pres.getSelectedRow();
        
          int id = (int) Table_pres.getValueAt(ligne_selectionee, 0);
          //System.out.println(id);
          PrestataireDAO pDAO =new PrestataireDAO();
          pDAO.deletePrestataire(id);
          
          this.actualiser();
          
        }   
    }//GEN-LAST:event_btnSupprimerActionPerformed

