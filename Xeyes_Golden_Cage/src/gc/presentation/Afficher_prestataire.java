    private void btnPlusDeDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPlusDeDetailsActionPerformed
        // TODO add your handling code here:
        if(Table_pres.getSelectedRow()>=0)
        {
        int ligne_selectionee=Table_pres.getSelectedRow();
          int id = (int) Table_pres.getValueAt(ligne_selectionee, 0);
          Afficher_detail_prestataire detail =new Afficher_detail_prestataire(id,this);
          
          detail.setVisible(true);
          this.setVisible(false);
          
          
        }
        else
        {
             JOptionPane.showMessageDialog(null,"Selectionnez un prestataire pour afficher ses details");
        }
        
    }//GEN-LAST:event_btnPlusDeDetailsActionPerformed

    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
        // TODO add your handling code here:
         
          if((Table_pres.getSelectedRow()) >= 0)
          {
              int ligne_selectionee=Table_pres.getSelectedRow();
              int id = (int) Table_pres.getValueAt(ligne_selectionee,0);
            
             PrestataireDAO pdao=new PrestataireDAO();
             Prestataire p = pdao.findPrestataireById(id);
             
             this.dispose();
              modifier_prestataire modif_pres=new modifier_prestataire(id,this);
              modif_pres.setVisible(true);   
          }
          else
          {
                JOptionPane.showMessageDialog(null,"Selectionnez un prestataire pour le modifier");
          }
    }//GEN-LAST:event_btnModifierActionPerformed

